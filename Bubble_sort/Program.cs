using System;

namespace Bubble_sort
{
    class Program
    {
        static void Main(string[] args)
        {
        }
        static void Bubble(int[]array, int temp)
        {
            for (int i = 0; i < array.Length; i++)
            {
                for (int j = 0; j < array.Length - 1; j++)
                {
                    if (array[j] > array[j + 1])
                    {
                        temp = array[j + 1];
                        array[j + 1] = array[j];
                        array[j] = temp;
                    }
                }
            }
        }
    }
}
